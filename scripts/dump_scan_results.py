#!/usr/bin/env python3
"""Utility to ingest scanner JSON output into the ``scan_corpus`` table.

This is a lightweight helper for the training-corpus infrastructure (see
storage_manager._init_db migration).  It simply reads one or more JSON files
and appends them to the database.

Usage::

    python scripts/dump_scan_results.py /path/to/scan.json [more.json ...]

The files can be the long/short scanner output (``last_scan_long.json``), any
custom scan file generated by a test harness, etc.
"""

import json
import sys
from pathlib import Path

# add project root to path
import os
sys.path.insert(0, os.path.abspath(os.path.join(os.path.dirname(__file__), "..")))

from modules.storage_manager import StorageManager


def ingest(db_path: Path, json_path: Path) -> None:
    if not json_path.exists():
        print(f"Skipping missing file: {json_path}")
        return
    try:
        with open(json_path, "r", encoding="utf-8") as f:
            payload = json.load(f)
    except Exception as e:
        print(f"Failed to load {json_path}: {e}")
        return

    storage = StorageManager(db_path)
    conn = storage._get_connection()
    cursor = conn.cursor()
    try:
        cursor.execute(
            "INSERT INTO scan_corpus (symbol, timestamp, data_json) VALUES (?, ?, ?)",
            (
                payload.get("symbol", "UNKNOWN"),
                payload.get("timestamp", ""),
                json.dumps(payload),
            ),
        )
        conn.commit()
        print(f"Inserted scan result from {json_path} into scan_corpus")
    except Exception as e:
        print(f"Database insert failed for {json_path}: {e}")
    finally:
        conn.close()


if __name__ == "__main__":
    if len(sys.argv) < 3:
        print(__doc__)
        sys.exit(1)

    db = Path("data/state/fancybot.db")
    for path_str in sys.argv[1:]:
        ingest(db, Path(path_str))
